const dictionaries: Record<StoredLanguagePreference, Record<keyof typeof fr, string>> = {
  fr,
  en,
};

export type TranslationKey = keyof typeof fr;
export type AppLocale = 'en-US' | 'fr-FR';

const interpolate = (template: string, params?: TranslationParams) => {
  if (!params) {
    return template;
  }

  return Object.entries(params).reduce((result, [key, value]) => {
    return result.replaceAll(`{${key}}`, String(value));
  }, template);
};

export const translate = (
  language: StoredLanguagePreference,
  key: TranslationKey,
  params?: TranslationParams,
) => {
  const primary = dictionaries[language]?.[key];
  const fallback = dictionaries.fr[key];
  const message = primary || fallback || key;

  return interpolate(message, params);
};

export const getLocaleForLanguage = (
  language: StoredLanguagePreference,
): AppLocale =>
  language === 'en' ? 'en-US' : 'fr-FR';

